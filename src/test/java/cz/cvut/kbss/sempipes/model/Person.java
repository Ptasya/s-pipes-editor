package cz.cvut.kbss.sempipes.model;

import cz.cvut.kbss.jopa.model.annotations.Id;
import cz.cvut.kbss.jopa.model.annotations.OWLClass;
import cz.cvut.kbss.jopa.model.annotations.OWLDataProperty;
import cz.cvut.kbss.sempipes.TestVocabulary;

import java.net.URI;

@OWLClass(iri = TestVocabulary.PERSON)
public class Person {

    @Id
    public URI uri;

    @OWLDataProperty(iri = TestVocabulary.FIRST_NAME)
    private String firstName;

    @OWLDataProperty(iri = TestVocabulary.LAST_NAME)
    private String lastName;

    public URI getUri() {
        return uri;
    }

    public void setUri(URI uri) {
        this.uri = uri;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }
}
